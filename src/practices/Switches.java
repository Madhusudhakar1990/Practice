package practices;

public class Switches 
{

	public static void main(String[] args) 
	{
		
		
		int day = 5;
		
		String dayString = null;

		switch(day)
		{
		
		case 1:
			dayString = "Monday";
			break;
		
		
			
		case 2:
			dayString = "Tuesday";
			break;
		
		
			
		case 3:
			dayString = "Wednesday";
			break;
		
			
		case 4:
			dayString = "Thursday";
			break;
		
		}
		System.out.println(dayString);
	}

}
